let lang = localStorage.getItem('gymgg-lang') || 'es';

function t(k){ return T[lang][k] || T['es'][k] || k; }

function setLang(l){
  lang = l;
  localStorage.setItem('gymgg-lang', l);
  ['btn-es','btn-en','app-btn-es','app-btn-en'].forEach(id=>{
    const el = document.getElementById(id);
    if(el) el.classList.toggle('active', id.includes(l));
  });
  document.querySelectorAll('[data-i18n]').forEach(el=>{
    const k = el.getAttribute('data-i18n');
    el.textContent = t(k);
  });
  if(window.currentUser) renderAll();
}
